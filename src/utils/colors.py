        if getattr(ColorCode, name, None):
            return getattr(ColorCode, name)
        else:
            candidate = []
            for slot in ColorCode.__slots__:
                if name.lower() in slot.lower():
                    candidate.append(slot)
            if len(candidate) == 1:
                return getattr(ColorCode, candidate[0])
            elif len(candidate) > 1:
                raise AttributeError(f"Color '{name}' is ambiguous, candidates are {candidate}")
            raise AttributeError(f"Color '{name}' not found")


