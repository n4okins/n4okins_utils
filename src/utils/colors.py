class RGBColorType(tuple):
    def __new__(cls, color: "RGBColorType"):
        assert len(color) == 3, "Color must be a 3-tuple"
        assert all(0 <= c <= 255 for c in color), "Color must be in range 0-255"
        return super().__new__(cls, color)


class HEXColorType(str):
    def __new__(cls, color: str):
        if color.startswith("#"):
            color = color[1:]
        assert len(color) == 6, "Color must be a 6-character string"
        assert all(c in "0123456789abcdef" for c in color.lower()), "Color must be a hexadecimal string"
        return super().__new__(cls, color)


class TerminalEffectType(int):
    def __new__(cls, effect: int = 0):
        assert 0 <= effect <= 9, "Effect must be in range 0-8"
        return super().__new__(cls, effect)


class Color:
    def __init__(
        self,
        color: RGBColorType | HEXColorType | tuple[int, int, int] | str,
        name: Optional[str] = None,
    ):
        if isinstance(color, str):
            color = HEXColorType(color)
        if isinstance(color, tuple):
            color = RGBColorType(color)

        self._rgb = self.convert_any_to_rgb(color)
        self._hex = self.convert_any_to_hex(color)
        self._name = name

    @property
    def name(self) -> Optional[str]:
        return self._name

    @property
    def rgb(self) -> RGBColorType:
        return self._rgb

    @property
    def hex(self) -> HEXColorType:
        return self._hex

    def reverse(self) -> "Color":
        ""
        return Color(tuple(255 - c for c in self.rgb))

    def __eq__(self, other: object) -> bool:
        if not isinstance(other, Color):
            raise NotImplementedError
        return self.rgb == other.rgb

    def __repr__(self) -> str:
        s = "Color("
        if self.name:
            s += f'"{self.name}", '
        s += f"RGB:{self.rgb}, HEX:#{self.hex})"
        return s

    def __iter__(self):
        return iter(self.rgb)

    @staticmethod
    def convert_any_to_rgb(
        color: Union[RGBColorType, HEXColorType, tuple[int, int, int], str],
    ) -> RGBColorType:
        if isinstance(color, tuple):
            return RGBColorType(color)
        elif isinstance(color, str):
            rgb: tuple[int, int, int] = tuple(int(color[i : i + 2], 16) for i in range(0, 6, 2))  # type: ignore
            assert len(rgb) == 3, "Color must be a 3-tuple"
            return RGBColorType(rgb)
        else:
            return color

    @staticmethod
    def convert_any_to_hex(
        color: Union[RGBColorType, HEXColorType, tuple[int, int, int], str],
    ) -> HEXColorType:
        if isinstance(color, tuple):
            return HEXColorType("".join(f"{c:02x}" for c in color))
        elif isinstance(color, str):
            return HEXColorType(color)
        else:
            return color


class ColoredStr:
    """Decorate a string with color and effects for terminal output.
    ColoredStr is a subclass of str that allows you to create colored text in the terminal.

    Args:
        text (str): The text to be colored.
        fg (Color, optional): The foreground color of the text. Defaults to None.
        bg (Color, optional): The background color of the text. Defaults to None.
        effects (Union[TerminalEffectType, int, Iterable[TerminalEffectType], Iterable[int]], optional): The effects to be applied to the text. Defaults to None.
        foreground_color (Color, optional): The foreground color of the text. Defaults to None.
        background_color (Color, optional): The background color of the text. Defaults to None.

    Returns:
        ColoredStr: The colored text.

    Examples:
        >>> text = ColoredStr("Hello, World!", ColorCode.GREEN)
        >>> print(text)
        Hello, World! # green text

        >>> text = ColoredStr("Hello, World!", ColorCode.RED, ColorCode.BLACK)
        >>> print(text)
        Hello, World! # red text on black background

        >>> text = ColoredStr("Hello, World!", bg=ColorCode.GREEN, effects=EffectCode.BOLD)
        >>> print(text)
        Hello, World! # green text with bold effect

        >>> text = ColoredStr("Hello, World!", fg=ColorCode.RED, bg=ColorCode.BLACK, effects=[EffectCode.BOLD, EffectCode.UNDERLINE, EffectCode.ITALIC])
        >>> print(text)
        Hello, World! # red text on black background with bold, underline, and italic effects

        >>> text = ColoredStr("Hello, World!", foreground_color=ColorCode.RED, background_color=ColorCode.BLACK, effects=[1, 4, 3])
        >>> print(text)
        Hello, World! # red text on black background with bold, underline, and italic effects

    """

    def __init__(
        self,
        text: str,
        fg: Optional[Color] = None,
        bg: Optional[Color] = None,
        *,
        effects: Optional[TerminalEffectType | int | Iterable[TerminalEffectType] | Iterable[int]] = None,
        foreground_color: Optional[Color] = None,
        background_color: Optional[Color] = None,
    ):
        if fg:
            foreground_color = fg
        if bg:
            background_color = bg
        self._foreground = ""
        self._background = ""
        self._inner_text = text
        if foreground_color:
            if not isinstance(foreground_color, Color):
                foreground_color = Color(self.convert_any_to_rgb(foreground_color))
        if effects:
            if not isinstance(effects, Iterable):
                effects = [effects]
            effects = [str(TerminalEffectType(e)) for e in effects if isinstance(e, int)]
            if foreground_color:
                r, g, b = foreground_color.rgb
                self._foreground = f"\033[{';'.join(effects)};38;2;{r};{g};{b}m"
        else:
            if foreground_color:
                r, g, b = foreground_color.rgb
                self._foreground = f"\033[38;2;{r};{g};{b}m"

        if background_color:
            if not isinstance(background_color, Color):
                background_color = Color(self.convert_any_to_rgb(background_color))
            r, g, b = background_color.rgb
            self._background = f"\033[48;2;{r};{g};{b}m"

        self._reset = "\033[0m"

    @property
    def text(self) -> str:
        return self._inner_text

    def __len__(self) -> int:
        return len(self._inner_text)

    def __str__(self) -> str:
        return f"{self._foreground}{self._background}{self._inner_text}{self._reset}{self._reset}"

    def __add__(self, other: str) -> str:
        return str(self) + str(other)

    def __radd__(self, other: str) -> str:
        return str(other) + str(self)

    @staticmethod
    def convert_any_to_rgb(color: Color) -> Color:
        if isinstance(color, tuple):
            return RGBColorType(color)
        elif isinstance(color, str):
            return RGBColorType(tuple(int(color[i : i + 2], 16) for i in range(0, 6, 2)))
        else:
            return color


