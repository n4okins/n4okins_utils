    @staticmethod
    def search(name: str) -> Color:
        """Search color by name
        Args:
            name (str): color name
        Returns:
            Color: color object
        Raises:
            AttributeError: if color not found

        Examples:
        >>> JapaneseColorCode.search("朱緋")
        Color("朱緋", RGB:(186, 38, 54), HEX:#BA2636)

        >>> JapaneseColorCode.search("わかば")
        Color("わかばいろ", RGB:(185, 208, 139), HEX:#B9D08B)

        >>> JapaneseColorCode.search("sakura")
        AttributeError: Color 'sakura' is ambiguous, candidates are ['SAKURAIRO', 'SAKURANEZU']
        """
        if getattr(JapaneseColorCode, name, None):
            return getattr(JapaneseColorCode, name)
        else:
            candidate = []
            for slot in JapaneseColorCode.__slots__:
                if name.lower() in slot.lower():
                    candidate.append(slot)
            if len(candidate) == 1:
                return getattr(JapaneseColorCode, candidate[0])
            elif len(candidate) > 1:
                raise AttributeError(f"Color '{name}' is ambiguous, candidates are {candidate}")
            raise AttributeError(f"Color '{name}' not found")


